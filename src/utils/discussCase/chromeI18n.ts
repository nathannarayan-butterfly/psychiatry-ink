export type DiscussChromeKey = keyof (typeof CHROME_I18N)['de']

/** Narrow any stored UI language to a supported chrome locale (DE fallback). */
export function resolveDiscussChromeLocale(language: string | undefined): DiscussChromeLocale {
  if (language === 'en' || language === 'fr' || language === 'es') return language
  return 'de'
}

