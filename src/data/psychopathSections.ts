export function clonePsychopathSections(): WorkspaceSectionTemplate[] {
  return defaultPsychopathSections.map((section) => ({
    ...section,
    checklistItems: section.checklistItems?.map((item) => ({ ...item })),
  }))
}

export function isLegacyPsychopathChecklistSections(sections: WorkspaceSectionTemplate[]): boolean {
  return sections.some(
    (section) =>
      section.id === 'bewusstsein-orientierung' ||
      section.id === 'aufmerksamkeit-konzentration' ||
      section.id === 'krankheitseinsicht',
  )
}
