export function translateSettingsWorkspaceUi(language: UiLanguage, key: SettingsWorkspaceUiKey): string {
  return settingsWorkspaceUiTranslations[key][language] ?? settingsWorkspaceUiTranslations[key].de
}

export function formatSettingsWorkspaceUi(
  language: UiLanguage,
  key: SettingsWorkspaceUiKey,
  vars: Record<string, string | number>,
): string {
  let text = translateSettingsWorkspaceUi(language, key)
  for (const [name, value] of Object.entries(vars)) {
    text = text.replaceAll(`{${name}}`, String(value))
  }
  return text
}
